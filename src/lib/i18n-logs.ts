/**
 * Holt einen Log-String in der gewünschten Sprache, mit EN-Fallback.
 *
 * @param lang   ioBroker-Systemsprache (`'en'`, `'de'`, …) — beliebiger String
 *               toleriert, Fallback auf `en` wenn unbekannt.
 * @param key    Übersetzungs-Schlüssel aus {@link LOG_STRINGS}.
 * @param params Token-Werte für `{name}`-Platzhalter im Template.
 */
export function tLog(
    lang: string,
    key: keyof typeof LOG_STRINGS,
    params?: Record<string, string | number | null | undefined>,
): string {
    const langKey = (SUPPORTED_LANGS as readonly string[]).includes(lang) ? (lang as Lang) : 'en';
    const bundle = LOG_STRINGS[key];
    const template = bundle[langKey] ?? bundle.en;
    return fmt(template, params);
}
