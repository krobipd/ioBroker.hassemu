/**
 * Lokalisierte Log-Strings — info/warn/error landen im ioBroker-Admin-Log,
 * also User-Sicht. Übersetzungen analog zu admin/i18n/<lang>/translations.json
 * in 11 Sprachen (en/de/ru/pt/nl/fr/it/es/pl/uk/zh-cn).
 *
 * Aktive Sprache wird einmal in `main.onReady` aus `system.config.language`
 * gelesen und an alle Klassen mit log-output durchgereicht. Sprachwechsel im
 * Admin braucht Adapter-Restart — akzeptabel weil User nicht alle paar Minuten
 * die Sprache wechselt.
 *
 * Debug-Logs bleiben EN (sind Maintainer-Diagnose, nicht User-sichtbar bei
 * Default-Loglevel). Stack-Traces bleiben in der Original-Form (Code-Pfade
 * sind nicht übersetzbar).
 */

const SUPPORTED_LANGS = ['en', 'de', 'ru', 'pt', 'nl', 'fr', 'it', 'es', 'pl', 'uk', 'zh-cn'] as const;
type Lang = (typeof SUPPORTED_LANGS)[number];

/**
 * Token-Substitution: `{name}` im Template wird durch `params.name` ersetzt.
 * Akzeptiert `null`/`undefined` explizit — Caller müssen für „kein Wert" nicht
 * vorab branchen, der Helper rendered `null` als "(none)" / `undefined` als
 * Platzhalter so wie er war.
 *
 * @param template Log-String mit `{key}`-Platzhaltern (English oder lokalisiert).
 * @param params   Werte-Map für die Platzhalter; null → `(none)`, undefined →
 *                 Token-Literal (Token nicht ersetzt).
 */
function fmt(template: string, params?: Record<string, string | number | null | undefined>): string {
    if (!params) {
        return template;
    }
    return template.replace(/\{(\w+)\}/g, (_, key: string) => {
        const v = params[key];
        if (v === null) {
            return '(none)';
        }
        if (v === undefined) {
            return `{${key}}`;
        }
        return String(v);
    });
}

